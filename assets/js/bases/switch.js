
// En elk Switch la evaluacion corresponde a un triple igual ====
// que evalua el valor que tiene y el tipo de dato.
const dia = 2;

switch( dia ) {
    case 0:
        console.log('Domingo');
        break;
    case 1:
        console.log('Lunes');
        break;
    case 2:
        console.log('Martes');
        break;
    default:
        console.log(' No es Domingo, Lunes, ni Martes');
}